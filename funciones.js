function mostrarRegistro() {
    var x = document.getElementById("form_agregar");
    if (x.style.display === "none") {
        x.style.display = "block";
    } else {
        x.style.display = "none";
    }
}

function ocultarRegistro() {
    var x = document.getElementById("form_agregar");
    if (x.style.display === "none") {
        x.style.display = "block";
    } else {
        x.style.display = "none";
    }
}